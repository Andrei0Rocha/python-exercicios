# Velocidade Média:

# • Solicite a distância percorrida e o tempo gasto.

# • Calcule a velocidade média usando a fórmula: velocidade
# = distância / tempo.

distancia = float((input("Digite a distancia pecorrida pelo veiculo: ")))
tempo = float(input("Digite o tempo gasto: "))

velocidadeMedia = distancia / tempo
print(f'A velocidade media do veiculo foi de {velocidadeMedia} Km/h') 