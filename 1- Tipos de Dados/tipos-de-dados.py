# Área do Retângulo:

# Peça ao usuário para inserir a base e a altura do
# retângulo. Calcule a área utilizando a fórmula:

# área = base * altura.

# Exiba o resultado na tela.

base = int(input("Digite o tamanho da base do Retangulo: "))
altura = int(input("Digite a altura do Retangulo: "))
area = base * altura
print(f'A área do retangulo com as medidas informadas por você é {area}')
